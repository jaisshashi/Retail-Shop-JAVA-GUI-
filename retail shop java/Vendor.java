    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add vendor button
        int log=0;
          try {
             Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/myshop","root","");
            Statement s=con.createStatement();
            String query="select * from vendor";
            ResultSet rs;
            rs=s.executeQuery(query);
            while(rs.next()){
                if((mobile.getText()).equals(rs.getString("mobile"))){
                    JOptionPane.showMessageDialog(null,"This Mobile number is already added");
                    mobile.setText("");
                    vname.setText("");
                   // jTextField3.setText("");
                    gstnumber.setText("");
                    jTextField5.setText("");
                    
                    break;
                }
                else{
                    log=0;
                            
                }
            }
            if(log==0)
            {
                PreparedStatement st=con.prepareStatement("insert into vendor(mobile,name,gstnumber,address)"+ "values(?,?,?,?)");
                    st.setString(1, mobile.getText());
                    st.setString(2, vname.getText());
//                    st.setString(3, jTextField3.getText());
                    st.setString(3, gstnumber.getText());
                    st.setString(4, jTextField5.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Record Saved...");
            }
             else{
              JOptionPane.showMessageDialog(null, "Item Exists...","",JOptionPane.ERROR_MESSAGE);
               
            }
          }catch(Exception e){
              JOptionPane.showMessageDialog(null, e);
          }
           jButton1.setVisible(true);
        jButton2.setVisible(false);
        jButton3.setVisible(false);
         jlabel4.setVisible(true);
        vadd.setVisible(true);
//        vmail.setVisible(true);
        vgst.setVisible(true);
        vname.setVisible(true);
    //    jTextField3.setVisible(true);
        gstnumber.setVisible(true);
        jTextField5.setVisible(true);
        mobile.setText("");
        vname.setText("");
        gstnumber.setText("");
        jTextField5.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

