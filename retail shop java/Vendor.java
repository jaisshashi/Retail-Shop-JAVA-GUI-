    private void mobileKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mobileKeyPressed
         char c=evt.getKeyChar();
         int len=mobile.getText().length();
        if(!Character.isDigit(c) || len>=10)
        {
            mobile.setEditable(false);
           
            mobileval.setVisible(true);
             mobileval.setText("Invalid Character typed...");
        }
        else{
            mobile.setEditable(true);
            mobileval.setVisible(false);
          
                    
        }
    }//GEN-LAST:event_mobileKeyPressed

